package bank.entity;

public class Year {
	private int id;
	private int year;
	
	public Year(){}
	public Year(int year){
		this.year = year;
	}
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	

}
